def exclude(start,exclude)->list[IPv4Network|IPv6Network]:
    result = [ip_network(start)]
    for x in exclude:
        n = ip_network(x)
        new = []
        for y in result:
            if y.overlaps(n):
                if n.subnet_of(y):
                    new.extend(y.address_exclude(n))
            else:
                new.append(y)
        result = new
    return result

